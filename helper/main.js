const Classes = document.body.classList;
const themeTgl = document.getElementById("theme-tgl")

function getBrowserPreferredTheme() {
    const mediaQuery = window.matchMedia("(prefers-color-scheme: dark)");
    if (mediaQuery.matches) {
        Classes.add("dark");
        changeThemeIcon(true);
    }else {
        Classes.add("light");
        changeThemeIcon(false);
    }

    // return mediaQuery.matches ? "dark" : "light";
}

// true means dark
function changeThemeIcon(dark) {
    if (dark) {
        themeTgl.innerHTML = moon_svg;
    } else {
        themeTgl.innerHTML = sun_svg;
    }
}
getBrowserPreferredTheme()

themeTgl.addEventListener("click", () => {
    if (Classes.contains("dark")) {
        Classes.remove("dark");
        Classes.add("light");
        changeThemeIcon(false)
    } else {
        Classes.add("dark");
        Classes.remove("light");
        changeThemeIcon(true);
    }
})


// nav bar
let prevScrollpos = window.pageYOffset;
let navbar = document.getElementById("navbar");

window.onscroll = function() {
  let currentScrollPos = window.pageYOffset;
  
  if (prevScrollpos > currentScrollPos) {
    navbar.classList.remove("hide-nav");
    navbar.classList.add("show-nav");
  } else {
    navbar.classList.remove("show-nav");
    navbar.classList.add("hide-nav");
  }
  
  prevScrollpos = currentScrollPos;
}